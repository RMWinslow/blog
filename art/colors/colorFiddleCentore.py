def RGBtoHTML(r,g,b):
    if r=='nan':
        return 'ERROR'
    "input RGB on 0-155 scale, get hex code."
    hexnumber = 256*256*int(r) + 256*int(g) + int(b)
    hexcode = '#' + f'{hexnumber:x}'.zfill(6)
    return hexcode


        
def htmlToRGB(html):
    html = html.lstrip('#')
    r = str(int(html[0:2],16))
    g = str(int(html[2:4],16))
    b = str(int(html[4:],16)  )      
    return r,g,b 

